# 2.4 Maiúsculas e minúsculas: Use uma variável para representar o nome de uma pessoa e,
# em seguida, exiba o nome dessa pessoa em letras minúsculas, maiúsculas e as primeiras letras maiúsculas.]


nome = "Caio júlio césar"
print(nome.lower()) #tudo minúsculo
print(nome.upper()) #tudo maiúsculo
print(nome.capitalize()) #só a primeira lete maiúscula
print(nome.title()) #todo início de palavra maiúsculo